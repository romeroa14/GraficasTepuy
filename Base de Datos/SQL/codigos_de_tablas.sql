-- Tabla para las universidades
CREATE TABLE IF NOT EXISTS mapa_info_uni (
    id INT AUTO_INCREMENT PRIMARY KEY,
    code_uni VARCHAR(50) NOT NULL,
    pagina VARCHAR(255) NOT NULL,
    mision TEXT NOT NULL,
    vision TEXT NOT NULL,
    descripcion TEXT NOT NULL,
    portada VARCHAR(255) NOT NULL,
    logo BLOB NOT NULL,
    escudo BLOB NOT NULL,
    estado VARCHAR(255),
    municipio VARCHAR(255),
    direccion TEXT,
    tipo VARCHAR(50),
    marcador BLOB NOT NULL,
    coordenadas POINT,
    UNIQUE (code_uni) -- Asegura unicidad por el código de la universidad
);

-- Tabla para las autoridades
CREATE TABLE IF NOT EXISTS mapa_uni_autorid (
    id INT AUTO_INCREMENT PRIMARY KEY,
    universidad_id INT,
    nombre VARCHAR(255) NOT NULL,
    cedula INT NOT NULL,
    reseña TEXT NOT NULL,
    cargo VARCHAR(100) NOT NULL,
    foto BLOB NOT NULL,
    url_curriculum BLOB NOT NULL,
    FOREIGN KEY (universidad_id) REFERENCES universidades(id)
);

